<h1>IRIS Reasoner</h1>
      <p>IRIS - Integrated Rule Inference System is an extensible reasoning engine for expressive rule-based languages 
(WSML Core/Flight/Rule), as well as description logic based languages (WSML-DL). Currently IRIS supports reasoning 
with WSML Core/Flight.</p>

