package org.deri.iris.dbstorage;

public class DbStorageManagerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7351155170581505168L;

	public DbStorageManagerException() {
	}

	public DbStorageManagerException(String arg0) {
		super(arg0);
	}

	public DbStorageManagerException(Throwable arg0) {
		super(arg0);
	}

	public DbStorageManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
