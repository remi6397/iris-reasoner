<h1>IRIS Reasoner</h1>
 	<p>IRIS - Integrated Rule Inference System is an extensible 
	reasoning engine for expressive rule-based languages. 
	Currently IRIS supports reasoning with stratified Datalog
	with built in predicates and extended data types.</p>
	  
  	<p>To learn more about the internals of the reasoner
	have a look at the <a href="foundations">theoretical results</a> 
	IRIS is	built upon.</p>
	
<h2>IRIS Applications</h2>
<p>IRIS is a generic Datalog reasoner available under the GNU lesser general
public licence (LGPL).
It has been developed with the aim to support reasoning over
<a href="http://www.wsmo.org/wsml/wsml-syntax">WSML</a> ontologies, 
but also can be used in many other contexts. See below for the 
use cases we know of</p>

<ul>
	<li><a href="wsml2reasoner">WSML Reasoner</a></li>
	<li><a href="rdfsreasoner">RDFS Reasoner</a></li>
</ul>

If you are using IRIS in another context, please inform us and we will 
be pleased to describe your application on this website. 

